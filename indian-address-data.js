// Function to populate dropdowns
function populateStates() {
    const stateSelects = document.querySelectorAll('#farmerState, #buyerState');
    stateSelects.forEach(select => {
        select.innerHTML = '<option value="">Select State</option>';
        Object.keys(indianAddressData).forEach(state => {
            select.innerHTML += `<option value="${state}">${state}</option>`;
        });
    });
}

function populateDistricts(stateName, districtSelectId) {
    const districtSelect = document.getElementById(districtSelectId);
    districtSelect.innerHTML = '<option value="">Select District</option>';
    
    if (stateName && indianAddressData[stateName]) {
        Object.keys(indianAddressData[stateName]).forEach(district => {
            districtSelect.innerHTML += `<option value="${district}">${district}</option>`;
        });
    }
}

function populateCities(stateName, districtName, citySelectId) {
    const citySelect = document.getElementById(citySelectId);
    citySelect.innerHTML = '<option value="">Select City/Town</option>';
    
    if (stateName && districtName && indianAddressData[stateName] && indianAddressData[stateName][districtName]) {
        indianAddressData[stateName][districtName].forEach(city => {
            citySelect.innerHTML += `<option value="${city}">${city}</option>`;
        });
    }
}